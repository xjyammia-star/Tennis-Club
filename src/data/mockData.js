// 格式化金额
export function formatCash(amount) {
  if (amount >= 10000) {
    return `¥${(amount / 10000).toFixed(amount % 10000 === 0 ? 0 : 1)}万`
  }
  return `¥${amount.toLocaleString()}`
}
